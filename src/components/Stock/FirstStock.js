import styled, { css } from 'styled-components/macro';

import './Slider.css';
import { IoMdArrowRoundForward } from 'react-icons/io';
import { IoArrowBack, IoArrowForward } from 'react-icons/io5';
import { Button } from '../Button/ButtonElements';
